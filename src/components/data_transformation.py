import sys 
import os
import numpy as np 
import pandas as pd 

from sklearn.compose import ColumnTransformer
from sklearn.impute import SimpleImputer
from sklearn.pipeline import Pipeline 
from sklearn.preprocessing import StandardScaler , OneHotEncoder

from src.exception import CustomException
from src.logger import logging
from src.utils import save_object 
 
from dataclasses import dataclass 

#Save path for preprocessing object
@dataclass 
class DataTransformationConfig:
    preprocessor_object_file_path = os.path.join('artifacts' , "preprocessor.pkl")

class DataTransformation:
        def __init__(self):
            self.data_transformation_config = DataTransformationConfig()
            
        def get_data_transformer_object(self):
            try:
                numerical_features = ['writing_score' , 'reading_score']
                categorical_features = [
                    'gender' , 
                    'race_ethnicity' , 
                    'parental_level_of_education' , 
                    'lunch' , 
                    'test_preparation_course'
                ]    
                
                num_pipeline = Pipeline(
                    steps = [
                        ('imputer' , SimpleImputer(strategy = 'median')) , 
                        ('scaler' , StandardScaler())
                    ]
                )
                
                cat_pipeline = Pipeline(
                    steps = [
                        ('imputer' , SimpleImputer(strategy = 'most_frequent' )) , 
                        ('one_hot_encoder' , OneHotEncoder()) , 
                        ('scaler' , StandardScaler(with_mean=False))
                    ]
                ) 
                
                logging.info(f"Categorical features : {categorical_features}")
                
                logging.info(f"Numerical features : {numerical_features}")
                
                #Apply numeric pipeline to numeric features
                #Apply categorical pipeline to categorical features
                
                preprocessor = ColumnTransformer([
                    ('num_pipeline' , num_pipeline , numerical_features) , 
                    ('cat_pipeline' , cat_pipeline , categorical_features)
                ])
                
                #return preprocessing object
                return preprocessor
            
            except Exception as e:
                raise CustomException(e , sys)
            
        def initiate_data_transformation(self , train_path , test_path):
            try:
                #these paths come from Data Ingestion
                train_df = pd.read_csv(train_path) 
                test_df = pd.read_csv(test_path)
                
                logging.info('Read train and test data completed')
                
                logging.info('Obtaining preprocessing object')
                
                #This variable holds the ColumnTransformer object
                preprocessing_object = self.get_data_transformer_object()
                
                target_column_name = 'math_score' 
                
                input_feature_train_df = train_df.drop(columns = [target_column_name] , axis = 1)
                target_feature_train_df = train_df[target_column_name] 
                
                input_feature_test_df = test_df.drop(columns = [target_column_name] , axis = 1)
                target_feature_test_df = test_df[target_column_name]
                
                logging.info(f"Applying preprocessing object on training dataframe and testing dataframe")
                
                #Applying preprocessing 
                input_feature_train_arr = preprocessing_object.fit_transform(input_feature_train_df)
                input_feature_test_arr = preprocessing_object.transform(input_feature_test_df)
                
                train_arr = np.c_[input_feature_train_arr , np.array(target_feature_train_df)]
                test_arr = np.c_[input_feature_test_arr , np.array(target_feature_test_df)]
                
                logging.info('Saved preprocessing object')
                
                #Saving pickle file
                save_object(
                    
                    file_path = self.data_transformation_config.preprocessor_object_file_path , 
                    obj = preprocessing_object
                )
                
                return (
                    train_arr , 
                    test_arr , 
                    
                )
                
            except Exception as e:
                raise CustomException(e , sys)
                    
                
                
                   
    